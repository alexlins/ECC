
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace EccInfra
{
    public class UserInfra
    {
        public UserInfra()
        {
            Usuarios usu = new Usuarios();
            usu.Nome = "";
            usu.IsActive = true;
            usu.Nivel = 1;
            using (var entity = new EccEntities())
            {
                entity.AddObject("Usuarios", usu);
                entity.SaveChanges();
            }
        }

    }
}
